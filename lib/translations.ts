export function translateToBangla(text: string, type: 'sampleTypes' | 'testElements' | 'units' | 'analysisTypes'): string {
  if (!text) return '';
  
  // If the text is already in Bangla (contains Bangla characters), return as is
  if (/[\u0980-\u09FF]/.test(text)) {
    return text;
  }

  // Try to find an exact match
  const translation = translations[type][text as keyof typeof translations[typeof type]];
  if (translation) {
    return translation;
  }

  // If no exact match is found, return the original text
  return text;
}

export function translateSampleType(type: string): string {
  return translateToBangla(type, 'sampleTypes');
}

export function translateTestElement(element: string): string {
  return translateToBangla(element, 'testElements');
}

export function translateUnit(unit: string): string {
  return translateToBangla(unit, 'units');
}

export function translateAnalysisType(type: string): string {
  return translateToBangla(type, 'analysisTypes');
}

// Function to convert numbers to Bangla numerals
export function numberToBangla(num: number): string {
  const banglaDigits = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯'];
  return num.toString().replace(/[0-9]/g, (match) => banglaDigits[parseInt(match)]);
}

// Function to convert number to Bangla words
export function numberToBanglaWords(num: number): string {
  const units = ['', 'এক', 'দুই', 'তিন', 'চার', 'পাঁচ', 'ছয়', 'সাত', 'আট', 'নয়', 'দশ', 'এগারো', 'বারো', 'তেরো', 'চৌদ্দ', 'পনেরো', 'ষোল', 'সতেরো', 'আঠারো', 'উনিশ', 'বিশ'];
  const tens = ['', '', 'বিশ', 'ত্রিশ', 'চল্লিশ', 'পঞ্চাশ', 'ষাট', 'সত্তর', 'আশি', 'নব্বই'];
  
  if (num === 0) return 'শূন্য';
  
  if (num < 20) return units[num];
  
  if (num < 100) {
    const unit = num % 10;
    const ten = Math.floor(num / 10);
    return unit === 0 ? tens[ten] : tens[ten] + ' ' + units[unit];
  }
  
  if (num < 1000) {
    const hundred = Math.floor(num / 100);
    const remainder = num % 100;
    return hundred === 1 ? 'একশত' + (remainder ? ' ' + numberToBanglaWords(remainder) : '') 
      : units[hundred] + 'শত' + (remainder ? ' ' + numberToBanglaWords(remainder) : '');
  }
  
  if (num < 100000) {
    const thousand = Math.floor(num / 1000);
    const remainder = num % 1000;
    return thousand === 1 ? 'এক হাজার' + (remainder ? ' ' + numberToBanglaWords(remainder) : '')
      : numberToBanglaWords(thousand) + ' হাজার' + (remainder ? ' ' + numberToBanglaWords(remainder) : '');
  }
  
  if (num < 10000000) {
    const lakh = Math.floor(num / 100000);
    const remainder = num % 100000;
    return lakh === 1 ? 'এক লক্ষ' + (remainder ? ' ' + numberToBanglaWords(remainder) : '')
      : numberToBanglaWords(lakh) + ' লক্ষ' + (remainder ? ' ' + numberToBanglaWords(remainder) : '');
  }
  
  const crore = Math.floor(num / 10000000);
  const remainder = num % 10000000;
  return crore === 1 ? 'এক কোটি' + (remainder ? ' ' + numberToBanglaWords(remainder) : '')
    : numberToBanglaWords(crore) + ' কোটি' + (remainder ? ' ' + numberToBanglaWords(remainder) : '');
} 

